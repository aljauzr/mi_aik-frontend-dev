    // Fungsi untuk mengambil data prestasi terbaru dari backend
    async function loadPrestasi() {
      const url = backend_url + 'prestasi';
      try {
        const response = await fetch(url);
        if (!response.ok) {
          throw new Error('Gagal mengambil data dari server');
        }

        const data = await response.json(); // Parsing data JSON
        const prestasiList = document.getElementById('prestasi-list');

        // Mengurutkan data berdasarkan id terbesar (prestasi terbaru)
        data.sort((a, b) => b.id - a.id);

        // Menambahkan maksimal 5 item dari data yang diterima
        data.slice(0, 5).forEach((item) => {
          const listItem = document.createElement('li');
          listItem.className = 'list-item';
          listItem.textContent = item.nama;
          prestasiList.appendChild(listItem);
        });
      } catch (error) {
        console.error('Terjadi kesalahan:', error);
      }
    }

    // Memuat data saat halaman selesai dimuat
    document.addEventListener('DOMContentLoaded', loadPrestasi);
  </script>
